package com.timmyroom.logicsample;

public class Calculator {
    public int add(int a, int b) {
        return a + b;
    }

    public int sub(int a, int b) {
        return a - b;
    }

    public float mul(int a, int b) {
        return a * b;
    }

    public float div(int a, int b) {
        return a / b;
    }
}